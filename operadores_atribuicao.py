saldo = 500

print(saldo)

saldo += 100    # Atribuindo saldo + 100
print(f"Atribuindo saldo + 100: {saldo}")

saldo -= 100    # Atribuindo saldo - 100
print(f"Atribuindo saldo - 100: {saldo}")

saldo *= 100    # Atribuindo saldo * 100
print(f"Atribuindo saldo * 100: {saldo}")

saldo /= 100    # Atribuindo saldo / 100
print(f"Atribuindo saldo / 100: {saldo}")

saldo //= 100   # Atribuindo saldo // 100
print(f"Atribuindo saldo // 100: {saldo}")

saldo %= 100    # Atribuindo saldo % 100
print(f"Atribuindo saldo % 100: {saldo}")

saldo **= 100   # Atribuindo saldo ** 100
print(f"Atribuindo saldo ** 100: {saldo}")


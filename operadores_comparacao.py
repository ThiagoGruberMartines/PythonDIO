# Igual ==
# Diferente !=
# Menor <
# Maior >
# Menor ou igual <=
# Maior ou igual >=


saldo = 500
fatura = 200

print(saldo == fatura) # Saldo é IGUAL a fatura?
print(saldo != fatura) # Saldo é DIFERENTE da fatura?

print(saldo < fatura) # Saldo é menor que a fatura?
print(saldo > fatura) # Saldo é maior que a fatura?
print(saldo <= fatura) # Saldo é menor ou igual a fatura?
print(saldo >= fatura) # Saldo é maior ou igual a fatura?